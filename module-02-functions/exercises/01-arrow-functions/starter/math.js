/**
 * Returns the number doubled.
 * @param {number} n
 */
export const double = (n) => {
  return n * 2;
};

/**
 * Returns the number squared (multiplied by itself).
 * @param {number} n
 */
export const square = (n) => {
  return n * n;
};

/**
 * Returns the sum of two numbers.
 * @param {number} a
 * @param {number} b
 */
export const add = (a, b) => {
  return a + b;
};
